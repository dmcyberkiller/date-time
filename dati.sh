#!/bin/bash 
uname=$USER
uc_uname=$(echo "$uname" | tr '[:lower:]' '[:upper:]')
echo "Hello! '$uc_uname' | Hope Your'e Fine" | lolcat
display_date_time() {
current_date_time=$(date +"%A %B-%d-%Y %I:%M:%S %p")
green='\033[0;32m'
yellow='\033[0;33m'
blue='\033[0;34m'
reset='\033[0m' # Reset text color
echo "${green}$(echo $current_date_time | awk '{print $1}')${reset}, ${yellow}$(echo $current_date_time | awk '{print $2}')${reset}, ${blue}$(echo $current_date_time | awk '{print $3}')${reset} ${blue}$(echo $current_date_time | awk '{print $4}')${reset}"
}
display_date_time
